#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct AudioFrameHeader {
    pub stream_id: String,
    pub stream_session_id: String,
    pub adapter_id: String,
    pub source_kind: SourceKind,
    pub seq: u64,
    pub format: PcmFormat,
    pub sample_rate_hz: u32,
    pub channels: u16,
    pub source_sample_start: u64,
    pub sample_count: u32,
    /// Adapter-clock timestamp for the first sample in this frame unless provenance says otherwise.
    pub source_capture_mono_ns: u64,
    /// Adapter-clock timestamp when this frame was serialized/sent.
    pub adapter_send_mono_ns: u64,
    pub timestamp_provenance: TimestampProvenance,
    pub preceding_source_gap: Option<SourceSampleGap>,
}

impl AudioFrameHeader {
    pub fn expected_payload_len(&self) -> Option<usize> {
        let samples = self.sample_count.checked_mul(self.channels as u32)?;
        let bytes = samples.checked_mul(self.format.bytes_per_sample())?;
        usize::try_from(bytes).ok()
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AudioFrame {
    pub header: AudioFrameHeader,
    pub payload: Vec<u8>,
}

impl AudioFrame {
    pub fn new(header: AudioFrameHeader, payload: Vec<u8>) -> Result<Self, ProtocolError> {
        let frame = Self { header, payload };
        frame.validate_payload_len()?;
        Ok(frame)
    }

    pub fn validate_payload_len(&self) -> Result<(), ProtocolError> {
        let expected = self
            .header
            .expected_payload_len()
            .ok_or(ProtocolError::PayloadLengthOverflow)?;
        if self.payload.len() != expected {
            return Err(ProtocolError::PayloadLengthMismatch {
                expected,
                actual: self.payload.len(),
            });
        }
        Ok(())
    }

    pub fn encode(&self) -> Result<Vec<u8>, ProtocolError> {
        self.validate_payload_len()?;
        let header_json = serde_json::to_vec(&self.header)?;
        if header_json.len() > MAX_JSON_HEADER_LEN {
            return Err(ProtocolError::HeaderTooLarge(header_json.len()));
        }

        let mut out = Vec::with_capacity(HEADER_FIXED_LEN + header_json.len() + self.payload.len());
        out.extend_from_slice(MAGIC);
        out.put_u8(VERSION);
        out.put_u32(header_json.len() as u32);
        out.extend_from_slice(&header_json);
        out.extend_from_slice(&self.payload);
        Ok(out)
    }

    pub fn decode(bytes: &[u8]) -> Result<Self, ProtocolError> {
        if bytes.len() < HEADER_FIXED_LEN {
            return Err(ProtocolError::FrameTooShort(bytes.len()));
        }

        let mut cursor = bytes;
        let mut magic = [0u8; 4];
        cursor.copy_to_slice(&mut magic);
        if &magic != MAGIC {
            return Err(ProtocolError::BadMagic(magic));
        }

        let version = cursor.get_u8();
        if version != VERSION {
            return Err(ProtocolError::UnsupportedVersion(version));
        }

        let header_len = cursor.get_u32() as usize;
        if header_len > MAX_JSON_HEADER_LEN {
            return Err(ProtocolError::HeaderTooLarge(header_len));
        }
        if cursor.remaining() < header_len {
            return Err(ProtocolError::TruncatedHeader {
                header_len,
                remaining: cursor.remaining(),
            });
        }

        let mut header_bytes = BytesMut::with_capacity(header_len);
        header_bytes.extend_from_slice(&cursor[..header_len]);
        cursor.advance(header_len);

        let header: AudioFrameHeader = serde_json::from_slice(&header_bytes)?;
        let frame = Self {
            header,
            payload: cursor.to_vec(),
        };
        frame.validate_payload_len()?;
        Ok(frame)
    }
}

