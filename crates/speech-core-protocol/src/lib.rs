#[derive(Debug, Error)]
pub enum ProtocolError {
    #[error("frame too short: {0} bytes")]
    FrameTooShort(usize),
    #[error("bad magic: {0:?}")]
    BadMagic([u8; 4]),
    #[error("unsupported protocol version: {0}")]
    UnsupportedVersion(u8),
    #[error("json header too large: {0} bytes")]
    HeaderTooLarge(usize),
    #[error("truncated header: wanted {header_len} bytes, only {remaining} remaining")]
    TruncatedHeader { header_len: usize, remaining: usize },
    #[error("payload length overflow")]
    PayloadLengthOverflow,
    #[error("payload length mismatch: expected {expected} bytes, got {actual}")]
    PayloadLengthMismatch { expected: usize, actual: usize },
    #[error("json error: {0}")]
    Json(#[from] serde_json::Error),
}

pub fn now_mono_ns() -> u64 {
    monotonic_duration().as_nanos().min(u128::from(u64::MAX)) as u64
}

pub fn monotonic_duration() -> Duration {
    #[cfg(target_os = "linux")]
    {
        let mut ts = libc::timespec {
            tv_sec: 0,
            tv_nsec: 0,
        };
        let rc = unsafe { libc::clock_gettime(libc::CLOCK_MONOTONIC, &mut ts) };
        if rc == 0 {
            return Duration::new(ts.tv_sec as u64, ts.tv_nsec as u32);
        }
    }

    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
}

pub fn ns_delta_to_ms(later_ns: u64, earlier_ns: u64) -> f64 {
    later_ns.saturating_sub(earlier_ns) as f64 / 1_000_000.0
}

pub fn capture_to_ingress_latency(
    daemon_ns: u64,
    source_capture_ns: u64,
    provenance: &TimestampProvenance,
) -> LatencyMeasurement {
    if provenance.clock_comparability == ClockComparability::Uncalibrated {
        return uncalibrated_latency();
    }
    if provenance.timestamp_quality != TimestampQuality::SourceCapture {
        return LatencyMeasurement {
            value_ms: None,
            status: LatencyStatus::TimestampQualityInsufficient,
            uncertainty_ms: None,
        };
    }
    if provenance.timestamp_semantics != TimestampSemantics::FirstSample {
        return LatencyMeasurement {
            value_ms: None,
            status: LatencyStatus::TimestampSemanticsUnsupported,
            uncertainty_ms: None,
        };
    }
    cross_clock_latency(daemon_ns, source_capture_ns, provenance)
}

pub fn adapter_send_to_ingress_latency(
    daemon_ns: u64,
    adapter_send_ns: u64,
    provenance: &TimestampProvenance,
) -> LatencyMeasurement {
    cross_clock_latency(daemon_ns, adapter_send_ns, provenance)
}

pub fn cross_clock_latency(
    daemon_ns: u64,
    adapter_ns: u64,
    provenance: &TimestampProvenance,
) -> LatencyMeasurement {
    match provenance.clock_comparability {
        ClockComparability::SameClock => LatencyMeasurement {
            value_ms: Some(ns_delta_to_ms(daemon_ns, adapter_ns)),
            status: LatencyStatus::SameClock,
            uncertainty_ms: None,
        },
        ClockComparability::EstimatedOffset => match provenance.estimated_daemon_offset_ns {
            Some(offset) => {
                let adjusted_adapter_ns = if offset >= 0 {
                    adapter_ns.saturating_add(offset as u64)
                } else {
                    adapter_ns.saturating_sub(offset.unsigned_abs())
                };
                LatencyMeasurement {
                    value_ms: Some(ns_delta_to_ms(daemon_ns, adjusted_adapter_ns)),
                    status: LatencyStatus::EstimatedOffset,
                    uncertainty_ms: provenance
                        .estimated_offset_uncertainty_ns
                        .map(|ns| ns as f64 / 1_000_000.0),
                }
            }
            None => uncalibrated_latency(),
        },
        ClockComparability::Uncalibrated => uncalibrated_latency(),
    }
}

fn uncalibrated_latency() -> LatencyMeasurement {
    LatencyMeasurement {
        value_ms: None,
        status: LatencyStatus::Uncalibrated,
        uncertainty_ms: None,
    }
}

pub fn frame_duration_samples(sample_rate_hz: u32, frame_ms: u32) -> u32 {
    sample_rate_hz.saturating_mul(frame_ms) / 1000
}

pub fn frame_payload_len(sample_count: u32, channels: u16, format: PcmFormat) -> Option<usize> {
    let samples = sample_count.checked_mul(channels as u32)?;
    let bytes = samples.checked_mul(format.bytes_per_sample())?;
    usize::try_from(bytes).ok()
}

#[cfg(test)]
mod tests {
    use super::*;

    fn header() -> AudioFrameHeader {
        AudioFrameHeader {
            stream_id: "stream".into(),
            stream_session_id: "session".into(),
            adapter_id: "adapter".into(),
            source_kind: SourceKind::Mic,
            seq: 42,
            format: PcmFormat::PcmS16Le,
            sample_rate_hz: 16_000,
            channels: 1,
            source_sample_start: 13_440,
            sample_count: 320,
            source_capture_mono_ns: 1_000,
            adapter_send_mono_ns: 2_000,
            timestamp_provenance: TimestampProvenance::uncalibrated(
                "host:test:monotonic",
                ClockDomain::HostMonotonic,
                TimestampQuality::SourceCapture,
            ),
            preceding_source_gap: Some(SourceSampleGap {
                dropped_samples: 320,
                dropped_frames_estimate: 1,
                adapter_total_dropped_samples: 320,
                adapter_total_dropped_buffers: 1,
                reason: "test".into(),
            }),
        }
    }

    #[test]
    fn frame_round_trips_with_payload_and_provenance() {
        let frame = AudioFrame::new(header(), vec![0; 640]).unwrap();
        let encoded = frame.encode().unwrap();
        let decoded = AudioFrame::decode(&encoded).unwrap();
        assert_eq!(decoded, frame);
        assert_eq!(
            decoded.header.timestamp_provenance.timestamp_semantics,
            TimestampSemantics::FirstSample
        );
        assert_eq!(
            decoded.header.preceding_source_gap.unwrap().dropped_samples,
            320
        );
    }

    #[test]
    fn uncalibrated_cross_host_latency_is_unknown() {
        let provenance = TimestampProvenance::uncalibrated(
            "host:sfnix:monotonic",
            ClockDomain::HostMonotonic,
            TimestampQuality::SourceCapture,
        );
        let latency = cross_clock_latency(10_000_000, 1_000_000, &provenance);
        assert_eq!(latency.value_ms, None);
        assert_eq!(latency.status, LatencyStatus::Uncalibrated);
    }

    #[test]
    fn same_clock_source_capture_latency_is_computed() {
        let provenance = TimestampProvenance::same_clock(
            "host:sfub:monotonic",
            ClockDomain::HostMonotonic,
            TimestampQuality::SourceCapture,
        );
        let latency = capture_to_ingress_latency(11_000_000, 1_000_000, &provenance);
        assert_eq!(latency.value_ms, Some(10.0));
        assert_eq!(latency.status, LatencyStatus::SameClock);
    }

    #[test]
    fn capture_latency_requires_source_capture_quality_when_clocks_are_comparable() {
        let provenance = TimestampProvenance::same_clock(
            "host:sfub:monotonic",
            ClockDomain::HostMonotonic,
            TimestampQuality::CallbackReceive,
        );
        let latency = capture_to_ingress_latency(11_000_000, 1_000_000, &provenance);
        assert_eq!(latency.value_ms, None);
        assert_eq!(latency.status, LatencyStatus::TimestampQualityInsufficient);
    }

    #[test]
    fn adapter_send_latency_does_not_require_source_capture_quality() {
        let provenance = TimestampProvenance::same_clock(
            "host:sfub:monotonic",
            ClockDomain::HostMonotonic,
            TimestampQuality::CallbackReceive,
        );
        let latency = adapter_send_to_ingress_latency(11_000_000, 1_000_000, &provenance);
        assert_eq!(latency.value_ms, Some(10.0));
        assert_eq!(latency.status, LatencyStatus::SameClock);
    }

    #[test]
    fn rejects_wrong_payload_size() {
        let err = AudioFrame::new(header(), vec![0; 12]).unwrap_err();
        assert!(matches!(err, ProtocolError::PayloadLengthMismatch { .. }));
    }

    #[test]
    fn computes_frame_payload_len() {
        assert_eq!(frame_payload_len(320, 2, PcmFormat::PcmF32Le), Some(2560));
    }
}
