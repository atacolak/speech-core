const MAGIC: &[u8; 4] = b"SCF1";
const VERSION: u8 = 1;
const HEADER_FIXED_LEN: usize = 4 + 1 + 4;
const MAX_JSON_HEADER_LEN: usize = 64 * 1024;

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum SourceKind {
    Mic,
    Discord,
    Browser,
    File,
    Synthetic,
    Other,
}

impl fmt::Display for SourceKind {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            SourceKind::Mic => write!(f, "mic"),
            SourceKind::Discord => write!(f, "discord"),
            SourceKind::Browser => write!(f, "browser"),
            SourceKind::File => write!(f, "file"),
            SourceKind::Synthetic => write!(f, "synthetic"),
            SourceKind::Other => write!(f, "other"),
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum PcmFormat {
    #[serde(rename = "pcm_s16le")]
    PcmS16Le,
    #[serde(rename = "pcm_f32le")]
    PcmF32Le,
}

impl PcmFormat {
    pub fn bytes_per_sample(self) -> u32 {
        match self {
            PcmFormat::PcmS16Le => 2,
            PcmFormat::PcmF32Le => 4,
        }
    }
}

impl fmt::Display for PcmFormat {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            PcmFormat::PcmS16Le => write!(f, "pcm_s16le"),
            PcmFormat::PcmF32Le => write!(f, "pcm_f32le"),
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
